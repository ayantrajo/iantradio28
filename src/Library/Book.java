    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
      String bookn = bname.getText();
    CategoryItem citem = (CategoryItem) cat.getSelectedItem();
    AuthorItem auth = (AuthorItem) author.getSelectedItem();
    PublisherItem pubs = (PublisherItem) publisher.getSelectedItem();

    String cont = contents.getText();
    String page = no.getText();
    String edit = editn.getText();

    
    if (bookn.isEmpty() || citem == null || auth == null || pubs == null || cont.isEmpty() || page.isEmpty() || edit.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return; 
    }

    try {
        pat = con.prepareStatement("INSERT INTO book(book, category, author, publisher, content, pages, edition) VALUES (?, ?, ?, ?, ?, ?, ?)");
        pat.setString(1, bookn);
        pat.setInt(2, citem.id);
        pat.setInt(3, auth.id);
        pat.setInt(4, pubs.id);
        pat.setString(5, cont);
        pat.setString(6, page);
        pat.setString(7, edit);

        int k = pat.executeUpdate();
        if (k == 1) {
            JOptionPane.showMessageDialog(this, "Book Created!");
            
            bname.setText("");
            cat.setSelectedItem(null);
            author.setSelectedItem(null);
            publisher.setSelectedItem(null);
            contents.setText("");
            no.setText("");
            editn.setText("");
            Book_Load();
        } else {
            JOptionPane.showMessageDialog(this, "Error!");
        }
    } catch (SQLException ex) {
        Logger.getLogger(Book.class.getName()).log(Level.SEVERE, null, ex);
    }
     
    }//GEN-LAST:event_jButton1ActionPerformed

     public void Book_Load(){
        
        int c;
        
        try {
            pat = con.prepareStatement("SELECT b.id, b.book, c.catname, a.name,p.name,b.content,b.pages, b.edition from book b JOIN category c ON b.category = c.id JOIN author a on b.author  = a.id JOIN publisher p ON b.publisher = p.id ");
            rst = pat.executeQuery();
            
            ResultSetMetaData rsd = rst.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rst.next()){
                Vector v2 = new Vector();
                for(int i = 1; i<= c; i++ ){
                    v2.add(rst.getString("b.id"));
                    v2.add(rst.getString("b.book"));
                    v2.add(rst.getString("c.catname"));
                    v2.add(rst.getString("a.name"));
                    v2.add(rst.getString("p.name"));
                    v2.add(rst.getString("b.content"));
                    v2.add(rst.getString("b.pages"));
                    v2.add(rst.getString("b.edition"));
                }
                d.addRow(v2);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Book.class.getName()).log(Level.SEVERE, null, ex);
        }
    
        
    }
    
     

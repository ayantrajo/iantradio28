    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String category = cat.getText();
    String status = stat.getSelectedItem().toString();

    if (category.isEmpty() || status.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Both category name and status are required.", "Missing Information", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        pat = con.prepareStatement("INSERT INTO category (catname, status) VALUES (?, ?)");
        pat.setString(1, category);
        pat.setString(2, status);

        int k = pat.executeUpdate();

        if (k == 1) {
            JOptionPane.showMessageDialog(this, "Category Created!");
            cat.setText("");
            stat.setSelectedItem(-1);
            cat.requestFocus();
            Category_Load();
        } else {
            JOptionPane.showMessageDialog(this, "Error!");
        }
    } catch (SQLException ex) {
        Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
    }

    }//GEN-LAST:event_jButton1ActionPerformed

     public void Category_Load(){
        
        int c;
        
        try {
            pat = con.prepareStatement("select * from category");
            rst = pat.executeQuery();
            
            ResultSetMetaData rsd = rst.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rst.next()){
                Vector v2 = new Vector();
                for(int i = 1; i<= c; i++ ){
                    v2.add(rst.getString("id"));
                    v2.add(rst.getString("catname"));
                    v2.add(rst.getString("status"));
                }
                d.addRow(v2);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
        }
    
        
    }
    
     

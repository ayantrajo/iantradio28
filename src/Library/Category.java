    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
     
      DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int selectIndex = jTable1.getSelectedRow();

    if (selectIndex == -1) { 
        JOptionPane.showMessageDialog(this, "Please select a row to delete.", "No Row Selected", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int id = Integer.parseInt(model.getValueAt(selectIndex, 0).toString());

    int confirmation = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this category?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
    if (confirmation == JOptionPane.YES_OPTION) {
        try {
            pat = con.prepareStatement("DELETE FROM category WHERE id = ?");
            pat.setInt(1, id);
            int k =  pat.executeUpdate();
            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Deleted Category!");
                cat.setText("");
                stat.setSelectedItem(-1);
                cat.requestFocus();
                Category_Load();
                jButton1.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "Error!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    }//GEN-LAST:event_jButton4ActionPerformed

